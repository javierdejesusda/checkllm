"""Task C2-zero: metric-vs-synthetic-truth correlation study.

This experiment quantifies how well :class:`TrajectoryMetric.overall`
predicts a synthetic ground-truth signal generated by the
:class:`StubAgent` noise injector. We replay each tau-bench reference
trajectory across five noise regimes and ten seeds. The synthetic label
is binary: ``success = 1`` iff the trajectory was generated under
``noise_level == "clean"`` (no corruption injected); ``success = 0`` for
any non-zero noise level.

For every trajectory we compute ``TrajectoryMetric.overall`` and the
four sub-scores. We then report Pearson r (point-biserial), Spearman
rho, and AUROC (Mann-Whitney based, no sklearn dependency) with 95%
percentile bootstrap CIs (``n_bootstrap`` resamples; default 1000)
overall, per domain, and per sub-score.

Outputs in ``output_dir``:

* ``scores.jsonl`` -- one row per trajectory: ``domain, task_id, seed,
  noise_level, score (overall), label``.
* ``summary.json`` -- aggregate statistics with CIs and the
  ``auroc_gate_passed`` flag (CI lower bound >= 0.70 per domain).
* ``manifest.json`` -- per-trajectory provenance rows in the same shape
  as Task C1-zero's manifest.

Run via:

    python benchmarks/paper/experiments/02_metric_vs_truth.py \
        --output-dir benchmarks/paper/results/02_metric_vs_truth

Note: because the file name starts with a digit it is not a valid
Python identifier; ``python -m benchmarks.paper.experiments.02_metric_vs_truth``
is therefore not supported. Invoke the script by file path instead.
"""

from __future__ import annotations

